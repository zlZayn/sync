        4
    ),
    认知偏差均值 = mean(
      (认知偏差_3_5_1_C +
        认知偏差_3_5_2_C +
        认知偏差_3_5_3_C +
        认知偏差_3_5_4_C) /
        4
    ),
    购买意愿均值 = mean(
      (购买意愿_3_6_1_C + 购买意愿_3_6_2_C + 购买意愿_3_6_3_C) / 3
    ),

    .groups = "drop"
  ) |>
  pivot_longer(cols = -聚类标签_C, names_to = "特征", values_to = "值") |>
  pivot_wider(names_from = 聚类标签_C, values_from = "值") |>
  gt() |>
  tab_header(
    title = "子女群体聚类特征剖面",
    subtitle = "K-means聚类方法"
  ) |>
  fmt_number(columns = where(is.numeric), decimals = 3) |>
  tab_style(
    style = cell_text(weight = "bold"),
    locations = cells_column_labels()
  ) |>
  tab_options(table.width = "100%", table.font.size = 12) |>
  gtsave("output/子女群体聚类特征剖面.rtf")


